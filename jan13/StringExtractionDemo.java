public class StringExtractionDemo{
	public static void main(String arga[]){
		String sourceStringObj = "First Second Third";
		System.out.println("We have source string:'First Second Third'");
		System.out.println("'sourceStringObj.substring(0)' gives: "+sourceStringObj.substring(0));
		System.out.println("'sourceStringObj.substring(0,5)' gives: "+sourceStringObj.substring(0,5));
		System.out.println("'sourceStringObj.substring(6,12)' gives: "+sourceStringObj.substring(6,12));
		System.out.println("'sourceStringObj.substring(13)' gives: "+sourceStringObj.substring(13));
		//System.out.println("'sourceStringObj.substring(0)' gives: "+sourceStringObj.substring(0));
	}//End of main method.
}//End of class.